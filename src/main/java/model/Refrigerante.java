package model;

public class Refrigerante {

    //declaracao das variaveis
    private String nome;
    private double preco;

    //construtor
    public Refrigerante(){}

    //getter e setter
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public double getPreco() {
        return preco;
    }
    public void setPreco(double preco) {
        this.preco = preco;
    }

}
